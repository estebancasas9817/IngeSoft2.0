package com.company.workpeace.ClasesAuxiliares.Firebase;

public class Rutinas {
    String edad, aaaaa, pais;

    public Rutinas() {
    }

    public Rutinas(String edad, String aaaaa, String pais) {
        this.edad = edad;
        this.aaaaa = aaaaa;
        this.pais = pais;
    }

    public String getEdad() {
        return edad;
    }

    public void setEdad(String edad) {
        this.edad = edad;
    }

    public String getAaaaa() {
        return aaaaa;
    }

    public void setAaaaa(String aaaaa) {
        this.aaaaa = aaaaa;
    }

    public String getPais() {
        return pais;
    }

    public void setPais(String pais) {
        this.pais = pais;
    }
}
